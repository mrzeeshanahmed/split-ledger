/**
 * Shared sidebar navigation items for all tenant dashboard pages.
 * Returns items with an `active` flag based on the current path.
 */
export function getTenantNavItems(currentPath: string) {
    const items = [
        { label: 'Dashboard', icon: <DashboardIcon />, href: '/dashboard' },
        { label: 'Expenses', icon: <ExpensesIcon />, href: '/dashboard/expenses' },
        { label: 'Groups', icon: <GroupsIcon />, href: '/dashboard/groups' },
        { label: 'Settlements', icon: <SettlementsIcon />, href: '/dashboard/settlements' },
        { label: 'API Keys', icon: <KeyIcon />, href: '/dashboard/api-keys' },
        { label: 'Analytics', icon: <AnalyticsIcon />, href: '/dashboard/analytics' },
        { label: 'Webhooks', icon: <WebhooksIcon />, href: '/dashboard/webhooks' },
        { label: 'Users', icon: <UsersIcon />, href: '/dashboard/users' },
        { label: 'Settings', icon: <SettingsIcon />, href: '/dashboard/settings' },
        { label: 'Billing', icon: <BillingIcon />, href: '/dashboard/billing' },
    ];

    return items.map((item) => ({
        ...item,
        active: currentPath === item.href || currentPath.startsWith(item.href + '/'),
    }));
}
